    private void verTodosLosClientes() {
        try {
            listaClientesBD = new ArrayList<>(c.obtenerClientes());
            listaClientesFiltrados = new ArrayList<>(listaClientesBD);
            actualizarTabla();
        } catch (Excepciones ex) {
            Logger.getLogger(GestionClientes.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void aplicarFiltros() {
        String mes = (String) cmbMes.getSelectedItem();
        String anio = (String) cmbAnio.getSelectedItem();
        String tipoClientes = ((TipoCliente) cmbTipoCliente.getSelectedItem()).getTipo();

        try {
            List<Cliente> listaFiltrada = c.obtenerClientesPorTipo(tipoClientes, obtenerNumeroMes(mes), Integer.parseInt(anio));

            if (!listaFiltrada.isEmpty()) {
                listaClientesFiltrados = listaFiltrada;
            }

            if (!listaClientesFiltrados.isEmpty()) {
                actualizarTabla();
            }
        } catch (Excepciones ex) {
            Logger.getLogger(ReportesCliente.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
    
    private void reiniciarFiltros() {
        cmbTipoCliente.setSelectedIndex(0);
        cmbMes.setSelectedIndex(0);
        cmbAnio.setSelectedIndex(0);
    }

    private void actualizarTabla() {
        modeloTabla.setRowCount(0);
        for (Cliente cliente : listaClientesFiltrados) {
            Object[] fila = {
                cliente.getId(),
                cliente.getNombre() + " " + cliente.getApellido(),
                cliente.getTelefono(),
                cliente.getCorreo(),
                tipoCliente(cliente.getTipoClienteId())
            };
            modeloTabla.addRow(fila);
        }
        tblClientes.setModel(modeloTabla);
    }

    private String tipoCliente(int tipoClienteId) {
        try {
            List<TipoCliente> tipoClientes = c.obtenerTiposCliente();
            for (TipoCliente tc : tipoClientes) {
                if (tc.getId() == tipoClienteId) {
                    return tc.getTipo();
                }
            }
        } catch (Excepciones e) {
            Logger.getLogger(GestionClientes.class.getName()).log(Level.SEVERE, null, e);
        }

        return "";
    }

    private int obtenerNumeroMes(String mes) {
        String[] meses = {"Enero", "Febrero", "Marzo", "Abril", "Mayo", "Junio",
            "Julio", "Agosto", "Septiembre", "Octubre", "Noviembre", "Diciembre"};
        for (int i = 0; i < meses.length; i++) {
            if (meses[i].equalsIgnoreCase(mes)) {
                return i + 1; // Enero = 1
            }
        }
        return 0;
    }


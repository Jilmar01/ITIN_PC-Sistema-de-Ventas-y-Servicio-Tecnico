    private void configuracionRol() {

        try {
            this.empleado = e.obtenerEmpleado(usuario.getId());

            String rol = usuario.getRol();
            String nombre = empleado.getNombre() + " " + empleado.getApellido();

            lblUser.setText(nombre);
            lblRol.setText(rol);

        } catch (Excepciones ex) {
            Logger.getLogger(ReportesCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void aplicarComboBox() {
        try {
            tipoCliente = c.obtenerTiposCliente();

            cmbTipoCliente.removeAllItems();
            cmbTipoCliente.addItem(new TipoCliente(0, "Tipo Cliente"));
            for (TipoCliente tc : tipoCliente) {
                cmbTipoCliente.addItem(tc);
            }

            // Si quieres llenar meses
            cmbMes.removeAllItems();
            cmbMes.addItem("Mes");
            String[] meses = {"Enero", "Febrero", "Marzo", "Abril", "Mayo", "Junio",
                "Julio", "Agosto", "Septiembre", "Octubre", "Noviembre", "Diciembre"};
            for (String m : meses) {
                cmbMes.addItem(m);
            }

            // Si quieres llenar años (ejemplo 2020–2025)
            cmbAnio.removeAllItems();
            cmbAnio.addItem("Año");
            for (int i = 2020; i <= 2025; i++) {
                cmbAnio.addItem(String.valueOf(i));
            }

        } catch (Excepciones ex) {
            Logger.getLogger(GestionClientes.class.getName()).log(Level.SEVERE, null, ex);
        }
    }


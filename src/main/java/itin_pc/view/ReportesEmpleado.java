    private void configuracionRol() {

        try {
            this.empleado = e.obtenerEmpleado(usuario.getId());

            String rol = usuario.getRol();
            String nombre = empleado.getNombre() + " " + empleado.getApellido();

            lblUser.setText(nombre);
            lblRol.setText(rol);

        } catch (Excepciones ex) {
            Logger.getLogger(ReportesEmpleado.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void aplicarComboBox() {
        try {

            List<Marca> marcas = p.obtenerMarcas();

            cmbMarcas.removeAllItems();
            cmbMarcas.addItem(new Marca(0, "Marcas"));

            for (Marca marca : marcas) {
                cmbMarcas.addItem(marca);
            }

        } catch (Excepciones ex) {
            Logger.getLogger(ReportesEmpleado.class.getName()).log(Level.SEVERE, null, ex);
        }
    }


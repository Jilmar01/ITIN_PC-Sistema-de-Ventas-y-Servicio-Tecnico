    private void verReporteEmpleadosConMasVentas() {
        try {
            if (reporteEmpleados == null) {
                reporteEmpleados = new ArrayList<>(e.empleadosConMasVentas());
            }
            actualizarTablaMasVentas();
        } catch (Excepciones ex) {
            Logger.getLogger(ReportesEmpleado.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void actualizarTablaMasVentas() {
        modeloTablaMasVentas.setRowCount(0);
        int posicion = 1;
        for (ReporteEmpleados reporte : reporteEmpleados) {
            Object[] fila = {
                    posicion,
                    reporte.getId(),
                    reporte.getNombre() + " " + reporte.getApellido(),
                    reporte.getNumeroVentas(),
                    reporte.getMontoTotal(),
                    obtenerUltimaVenta(reporte.getId()).getFecha(), };
            modeloTablaMasVentas.addRow(fila);
            posicion++;
        }
        tblReporteMasVentas.setModel(modeloTablaMasVentas);
    }

    private Venta obtenerUltimaVenta(int ventaId) {
        Venta venta = null;
        try {
            venta = v.obtenerVentaPorId(ventaId);
        } catch (Excepciones ex) {
            Logger.getLogger(ReportesEmpleado.class.getName()).log(Level.SEVERE, null, ex);
        }
        return venta;
    }

    private void verReporteServiciosTecnicos(String marca) {
        try {
            reporteServicioTecnico = new ArrayList<>(stc.obtenerReportesPorMarca(marca));
            actualizarTablaReparaciones();

        } catch (Excepciones ex) {
            Logger.getLogger(ReportesEmpleado.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void actualizarTablaReparaciones() {
        modeloTablaServicioTenico.setRowCount(0);
        for (ReporteServicioTecnico servicio : reporteServicioTecnico) {
            Object[] fila = {
                    servicio.getNombreProducto(),
                    servicio.getMarcaProducto(),
                    servicio.getTipoServicio(),
                    servicio.getFechaServicio(),
                    servicio.getProblemasReportados()
            };
            modeloTablaServicioTenico.addRow(fila);
        }
        tblReporteReparaciones.setModel(modeloTablaServicioTenico);
    }

